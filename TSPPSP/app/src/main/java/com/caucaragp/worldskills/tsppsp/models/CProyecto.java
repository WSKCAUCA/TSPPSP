package com.caucaragp.worldskills.tsppsp.models;

public class CProyecto {
    //Declaración de variables de la clase CProyecto
    private int id;
    private String name;
    private int time;

    //Constructor vacio
    public CProyecto() {
    }

    //Función para obtener la variable id
    public int getId() {
        return id;
    }

    //Método para ingresar la variable id
    public void setId(int id) {
        this.id = id;
    }

    //Función para obtener la variable name
    public String getName() {
        return name;
    }

    //Método para ingresar la variable id
    public void setName(String name) {
        this.name = name;
    }

    //Función para obtener la variable time
    public int getTime() {
        return time;
    }

    //Método para ingresar la variable id
    public void setTime(int time) {
        this.time = time;
    }

}
